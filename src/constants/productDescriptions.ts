/**
 * Extract filename from cloudinaryId
 * @param cloudinaryId - Full cloudinary path (e.g., "floreria/ramos-elegantes/Ramo1")
 * @returns filename without extension (e.g., "Ramo1")
 */
export function extractFilename(cloudinaryId: string): string {
  const segments = cloudinaryId.split('/');
  const filenameWithExt = segments[segments.length - 1];
  // Remove extension if present
  return filenameWithExt.replace(/\.(png|jpg|jpeg|webp)$/i, '');
}

/**
 * Get detailed description for a product based on its cloudinaryId
 * @param cloudinaryId - Full cloudinary path
 * @returns detailed description or null if not found
 */
export function getDetailedDescription(cloudinaryId: string): string | null {
  const filename = extractFilename(cloudinaryId);

  // Ramos Elegantes
  if (cloudinaryId.includes('ramos-elegantes')) {
    return ELEGANTES_DESCRIPTIONS[filename] || null;
  }

  // Ramos Clásicos
  if (cloudinaryId.includes('ramos-clasicos')) {
    if (cloudinaryId.includes('ramoEstilizado')) return ESTILIZADO_DESCRIPTIONS[filename] || null;
    if (cloudinaryId.includes('ramoGirasoles')) return GIRASOLES_DESCRIPTIONS[filename] || null;
    if (cloudinaryId.includes('ramoMix'))       return MIX_DESCRIPTIONS[filename] || null;
    if (cloudinaryId.includes('ramoRosas'))     return ROSAS_DESCRIPTIONS[filename] || null;
    if (cloudinaryId.includes('ramoTulipanes')) return TULIPANES_DESCRIPTIONS[filename] || null;
  }

  // Bodas de Ensueño
  if (cloudinaryId.includes('bodas-de-ensueno')) {
    if (cloudinaryId.includes('ramosNovia'))          return BODAS_RAMOS_NOVIA_DESCRIPTIONS[filename] || null;
    if (cloudinaryId.includes('arreglosCentrosDeMesa')) return BODAS_CENTROS_MESA_DESCRIPTIONS[filename] || null;
    if (cloudinaryId.includes('templo'))              return BODAS_TEMPLO_DESCRIPTIONS[filename] || null;
  }

  // Cumpleaños
  if (cloudinaryId.includes('cumpleanos')) {
    return CUMPLEANOS_DESCRIPTIONS[filename] || null;
  }

  // Quinceañera
  if (cloudinaryId.includes('quinceanera')) {
    if (cloudinaryId.includes('centroDeMesa')) return QUINCEANERA_CENTROS_DESCRIPTIONS[filename] || null;
    if (cloudinaryId.includes('ramos'))        return QUINCEANERA_RAMOS_DESCRIPTIONS[filename] || null;
    if (cloudinaryId.includes('templo'))       return QUINCEANERA_TEMPLO_DESCRIPTIONS[filename] || null;
  }

  // Celebraciones Especiales
  if (cloudinaryId.includes('celebraciones-especiales')) {
    if (cloudinaryId.includes('canastasFlores'))           return CELEBRACIONES_CANASTAS_DESCRIPTIONS[filename] || null;
    if (cloudinaryId.includes('centrosDeMesaFestivos'))    return CELEBRACIONES_CENTROS_DESCRIPTIONS[filename] || null;
    if (cloudinaryId.includes('detallesEnFormaDeCorazon')) return CELEBRACIONES_CORAZON_DESCRIPTIONS[filename] || null;
  }

  // Eventos Religiosos
  if (cloudinaryId.includes('eventos-religiosos')) {
    if (cloudinaryId.includes('arregloDeTemplo')) return EVENTOS_TEMPLO_DESCRIPTIONS[filename] || null;
    if (cloudinaryId.includes('bautizo'))         return EVENTOS_BAUTIZO_DESCRIPTIONS[filename] || null;
    if (cloudinaryId.includes('hermita'))         return EVENTOS_HERMITA_DESCRIPTIONS[filename] || null;
  }

  // Galería Funeraria
  if (cloudinaryId.includes('galeria-funeraria')) {
    if (cloudinaryId.includes('coronas'))      return FUNERARIA_CORONAS_DESCRIPTIONS[filename] || null;
    if (cloudinaryId.includes('cruces'))       return FUNERARIA_CRUCES_DESCRIPTIONS[filename] || null;
    if (cloudinaryId.includes('cubreCaja'))    return FUNERARIA_CUBRE_CAJA_DESCRIPTIONS[filename] || null;
    if (cloudinaryId.includes('pieCajaAltar')) return FUNERARIA_PIE_CAJA_DESCRIPTIONS[filename] || null;
  }

  return null;
}
